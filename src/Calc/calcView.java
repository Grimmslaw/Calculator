    // takes currentString and a char for operation as inputs, parses the terms, and does the calculation
    // return a double
    private double doCalculate(String input, char op) {
        double result = 0;
        
        String[] inputArray = input.split("\\+|-|\\*|/");
        
        double firstTerm = Double.parseDouble(inputArray[0]);
        double secondTerm = Double.parseDouble(inputArray[1]);
        
        if (op == '+') {
            result = firstTerm + secondTerm;
        } else if (op == '-') {
            result = firstTerm - secondTerm;
        } else if (op == '*') {
            result = firstTerm * secondTerm;
        } else if (op == '/') {
            result = firstTerm / secondTerm;
        }
        
        return result;
    }
    
    // trivial, but I couldn't wrap my head around doing it another way since pattern wasn't seeming to work with contains()
    private boolean hasPattern(String input) {
        if (input.contains("+")) {
            return true;
        } else if (input.contains("-")) {
            return true;
        } else if (input.contains("*")) {
            return true;
        } else if (input.contains("/")) {
            return true;
        } else {
            return false;
        }
    }
    
    // appends input char to end of currentString (if currentString isn't 0)
    // (otherwise empties currentString and appends char)
    private void doAppend(char currentChar) {
        if (!currentString.equals("0")) {
            currentString += currentChar;
        } else {
            currentString = "";
            currentString += currentChar;
        }
    }

